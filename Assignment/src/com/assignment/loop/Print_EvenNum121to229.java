package com.assignment.loop;
//1.WAP to print even numbers from 121 to 229 using for loop. 
public class Print_EvenNum121to229 {

	public static void main(String[] args) {
		int i;
		System.out.println("Even numbers from 121 to 229 are: ");
		for(i=121;i<=229;i++) {
			{
				if(i%2==0) {
					System.out.println(i);
				}
			}
		}

	}

}
